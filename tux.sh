routine() {
  if [[ $timeCap -ne $SECONDS ]]; then
    # Movement opportunity
    aux=$((timeCap%5))
    if [[ $aux -eq 0 ]]; then
      die=$((RANDOM%20+1))

      if [[ $die -le $tuxAiLevel ]]; then
        tux_movement
      fi

      if [[ $die -le $repAiLevel ]]; then
        rep_movement
      fi

      if [[ $whereAmI = "camera" ]] && [[ $succeededMov -eq 1 ]]; then
        ascii_transition_heavy
      fi

    fi

  # Power calculation.
    power+=$powerDrain
  fi

  timeCap=$SECONDS
  succeededMov=0
}

