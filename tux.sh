office() {
  break
  ascii_transition
  whereAmI="office"

  while :; do
    clear
    ascii_office_1
    routine
#    echo "timeCap: $timeCap"
#    echo "die: $die"
#    echo "tuxPos: $tuxPos"
#    echo "repPos: $repPos"
    echo "power: $power"
#    echo "powerDrain: $powerDrain"

    read -s -t 0.1 -n 1 input
    case "$input" in
      a) ascii_transition; vent;;
      s) ascii_transition; camera;;
      d) ascii_transition; door;;
    esac
  done
}

vent() {
  break

  if [[ $ventIsShut -eq 0 ]]; then
    ascii_vent
  else
    ascii_vent_shut
  fi

  while :; do
    routine

    read -s -t 0.1 -n 1 input
    case "$input" in
      d) office;;
      f)
        if [[ $ventIsShut -eq 0 ]]; then
          ventIsShut=1
          powerDrain+=-1
          ascii_vent_frame1; sleep 0.1
          ascii_vent_frame2; sleep 0.1
        else
          ventIsShut=0
          powerDrain+=1
          ascii_vent_frame2; sleep 0.1
          ascii_vent_frame1; sleep 0.1
        fi
        break;;
    esac
  done
  vent
}

door() {
  break
  whereAmI="door"

  if [[ $doorIsShut -eq 0 ]]; then
    ascii_door
  else
    ascii_door_shut
  fi

  while :; do
    routine
    if [[ $tuxPos = "door" ]] && [[ $doorIsShut -eq 0 ]]; then
      ascii_door_tux
    fi

    read -s -t 0.1 -n 1 input
    case "$input" in
      a) office;;
      f)
        if [[ $doorIsShut -eq 0 ]]; then
          doorIsShut=1
          powerDrain+=-1
          ascii_door_frame1; sleep 0.1
          ascii_door_frame2; sleep 0.1
        else
          doorIsShut=0
          powerDrain+=1
          ascii_door_frame2; sleep 0.1
          ascii_door_frame1; sleep 0.1
        fi
        break;;
    esac
  done
  door
}

camera() {
  break
  input=""
  whereAmI="camera"
  powerDrain+=-1

  while [[ $input != "s" ]]; do
    routine

    if [[ $tuxPos = $repPos ]] && [[ $tuxPos = $currentCam ]]; then
      ascii_"$currentCam"_both
    elif [[ $tuxPos = $currentCam ]]; then
      ascii_"$currentCam"_tux
    elif [[ $repPos = $currentCam ]]; then
      ascii_"$currentCam"_rep
    else
      ascii_"$currentCam"
    fi

    read -s -t 0.1 -n 1 input
    if [[ $input -ge 1 ]] && [[ $input -le 9 ]]; then # -n: is it filled? -z: is it null?
      ascii_transition_light
      currentCam="cam$input"
    fi
  done
  powerDrain+=1
  office
}

